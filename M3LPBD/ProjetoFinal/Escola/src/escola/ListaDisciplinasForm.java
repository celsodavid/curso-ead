    /**
     * Metodo responsavel por efetuar a exclusao de uma disciplina
     * @param evt 
     */
    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        try {
            String cmd = "DELETE FROM disciplina WHERE nome = '"+jComboBoxDisciplinas.getSelectedItem()+"'";
            
            Connection conn = ConexaoMySQL.getInstance().getConnection();
            conn.createStatement().executeUpdate(cmd);
            
            JOptionPane.showMessageDialog(rootPane, "Disciplina excluida com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            
            ListaDisciplinasForm listaDisciplinaForm = new ListaDisciplinasForm();
            listaDisciplinaForm.setVisible(true);
            dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir a disciplina!", "Erro", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

