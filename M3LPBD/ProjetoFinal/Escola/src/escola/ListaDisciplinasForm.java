    /**
     * Metodo responsavel por buscar e popular as Disciplinas no Combobox     
     */
    private void loadDisciplinas() {
        try {
            String cmd = "SELECT nome FROM disciplina";
            
            Connection conn = ConexaoMySQL.getInstance().getConnection();
            ResultSet res = conn.createStatement().executeQuery(cmd);
            
            while(res.next()) {
                String nome = res.getString("nome");
                // cast para pegar todos o metodos do combobox
                ((DefaultComboBoxModel)jComboBoxDisciplinas.getModel()).addElement(nome);
            }
            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

